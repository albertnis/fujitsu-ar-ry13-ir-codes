import { expect, it } from 'bun:test'
import { makeChecksum } from './makeChecksum'

it('generates the expected checksum', () => {
  const codes = [
    0x28, 0xc6, 0x00, 0x08, 0x08, 0x7f, 0x90, 0x0c, 0x07, 0x40, 0x0c, 0x00,
    0x00, 0x00, 0x04, 0x79,
  ]

  expect(makeChecksum(codes)).toEqual(0x79)
})
